LIST P=16F84A, F=INHX8M
INCLUDE <P16F84.INC>
__CONFIG 3FF5H

MAIN	BSF STATUS, RP0
		CLRF TRISB
		CLRF TRISA
		MOVLW 09H
		MOVWF OPTION_REG
		BCF STATUS, RP0
		
MOVLW 	38H
CALL 	SET_INSTRUCTION

MOVLW	41H
CALL	SET_DATA


SET_INSTRUCTION:
	MOVWF	PORTB
	MOVLW	05H
	MOVWF	PORTA
;	CALL	DELAY
	SLEEP
	MOVLW	04H
	MOVWF	PORTA
	RETURN
SET_DATA:
	MOVWF	PORTB
	MOVLW	07H
	MOVWF	PORTA
	;CALL	DELAY
	SLEEP
	MOVLW	06H
	MOVWF	PORTA
	RETURN


END