LIST P=16F84, F=INHX8M
INCLUDE<P16F84.INC>
__CONFIG 3FF5H


TEMP EQU 0DH




INIT	BSF STATUS,RP0
		MOVLW 0CH
		MOVWF OPTION_REG
		CLRF TRISB
		BCF STATUS, RP0
		
MAIN	
		MOVLW 01H
		MOVWF PORTB
		SLEEP
		INCF PORTB, F ;2
		SLEEP
		INCF PORTB, F ;3 
		SLEEP
		INCF PORTB, F ;4
		SLEEP
		INCF PORTB, F ;5
		SLEEP
		INCF PORTB, F ;6
		SLEEP
		INCF PORTB, F ;7
		SLEEP
		INCF PORTB, F ;8
		SLEEP
		INCF PORTB, F ;9
		SLEEP
		MOVLW 00H
		MOVWF PORTB ;0
		SLEEP
		GOTO MAIN
		
		
		


END