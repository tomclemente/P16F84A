LIST P=16F84A, F=INHX8M
INCLUDE<P16F84.INC>
__CONFIG 3FF9H

TEMP EQU 10H
DUMMY EQU 11H
COUNTER EQU 12H
IHAP EQU 13H
LOOP EQU 14H
COUNTCW EQU 15H
COUNTCCW EQU 16H
IHAP2 EQU 17H
ORG 0
GOTO INIT
ORG 4
GOTO INT_ROUTINE

INIT
	BSF STATUS, RP0
	MOVLW 1FH
	MOVWF TRISB
	CLRF TRISA
	BSF INTCON, GIE
	BSF INTCON, T0IE
	MOVLW 04H
	MOVWF OPTION_REG
	BCF STATUS, RP0
	MOVLW 00H
	MOVWF PORTB
	MOVLW 01H
	MOVWF IHAP
	MOVLW 04H
	MOVWF IHAP2
MAIN
	BCF PORTA, 0
	BTFSS PORTB, 4
	GOTO MAIN
	MOVLW 0FH
	ANDWF PORTB, W
	CALL KEYPAD
	MOVWF DUMMY
	MOVWF TEMP
	MOVLW D'15'
	CALL DELAY
	MOVLW 00H
	XORWF DUMMY, W
	BTFSC STATUS, Z
	GOTO MAIN
	BSF PORTA, 0

	GOTO INITRUN

CHECK	ADDWF PCL, F
		RETLW D'5' ;EVEN DECREMENT
		RETLW D'0' ;ODD INCREMENT
		RETURN 
;CW IF ODD
;CCW IF EVEN
INITRUN 
	BSF STATUS, RP0
	MOVLW 0FH
	MOVWF TRISB
	BCF STATUS, RP0
	MOVLW 03H
	MOVWF PORTA
RUN2
	MOVLW D'25'
	MOVWF LOOP
RUN	
	MOVLW 01H
	ANDWF DUMMY, W
	CALL CHECK
	MOVWF COUNTCW ;ODD
	MOVWF COUNTCCW ;EVEN
LY	INCF COUNTCW, F ;ODD 
	DECF COUNTCCW, F ;EVEN
	BTFSC DUMMY, 0 
	GOTO CW
	MOVFW COUNTCCW
	CALL STEP
	GOTO STEPVALUE

CW	MOVFW COUNTCW
	CALL STEP
STEPVALUE
	MOVWF PORTB

	MOVLW D'15'
	CALL DELAY

	BTFSC DUMMY, 0 ;SKIP IF EVEN
	GOTO CW2

	MOVLW 01H
	GOTO LOOP1
CW2 MOVLW 04H
	XORWF COUNTCW, W
	GOTO JUMP
LOOP1	
	XORWF COUNTCCW, W
	
JUMP
	BTFSS STATUS, Z
	GOTO LY
	DECFSZ LOOP, F
	GOTO RUN
	MOVLW D'62'
	CALL DELAY
	DECFSZ TEMP, F
	GOTO RUN2
	GOTO INIT









	
STEP	ADDWF PCL, F
		NOP
		RETLW 30H
		RETLW 60H
		RETLW 0C0H
		RETLW 90H
		RETURN

KEYPAD	ADDWF PCL, F
		RETLW 01H
		RETLW 02H
		RETLW 03H
		RETLW 00H
		RETLW 04H
		RETLW 05H
		RETLW 06H
		RETLW 00H
		RETLW 07H
		RETLW 08H
		RETLW 09H
		RETLW 00H
		RETLW 00H
		RETLW 00H
		RETLW 00H
		RETLW 00H
		RETURN


DELAY: MOVWF COUNTER
DELAY2 MOVLW 00H
	   XORWF COUNTER, W
	   BTFSS STATUS, Z
	   GOTO DELAY2
	   RETURN



INT_ROUTINE
	BCF INTCON, T0IF
	DECF COUNTER, F

	RETFIE

END